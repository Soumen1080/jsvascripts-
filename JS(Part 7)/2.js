// write an arrow function named isEven that takes a number as an argument and returns true if the number is even and false if the number is odd.

const isEven = (n) => n % 2 == 0;

console.log(isEven(4)); // true
console.log(isEven(7)); // false
console.log(isEven(0)); // true
console.log(isEven(-2)); // true